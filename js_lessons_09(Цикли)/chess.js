// Сформуйте рядок із шахівницею із вкладених циклів.
//  Для перекладу рядка використовуйте '\n'. Код повинен підтримувати легку зміну розмірів дошки.
//   Розмiр може бути непарним, наприклад 3x5.
//    Цi значення (розмiри дошки) повиннi бути в кодi тiльки в одному мiсце кожне.

// .#.#.#.#.#.#
// #.#.#.#.#.#.
// .#.#.#.#.#.#
// #.#.#.#.#.#.
// .#.#.#.#.#.#
// #.#.#.#.#.#.
// .#.#.#.#.#.#
// #.#.#.#.#.#.
// .#.#.#.#.#.#
// #.#.#.#.#.#.


let rows = prompt("Введіть кількість рядків");
let cols = prompt('Введіть кількість колонок');

let result = "";

// Зовнішній цикл для рядків
for (let i = 0; i < rows; i++) {
    // Внутрішній цикл для символів у рядку
    for (let j = 0; j < cols; j++) {
        // Шахівниця: додаємо "#" або ".", залежно від суми індексів
        result += (i + j) % 2 === 0 ? "#" : ".";
    }
    
    // Додаємо перенос рядка, крім останнього рядка
    if (i < rows - 1) {
        result += "\n";
    }
}

// Виводимо отриманий рядок
console.log(result);