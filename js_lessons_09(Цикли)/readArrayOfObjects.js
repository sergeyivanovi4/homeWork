// Напишіть функцію readArrayOfObjects, яка циклічно додає масив об'єкти, 
// які ввів користувач. Користувач вводить ключі та значення 
// (їх у кожному об'єкті може бути будь-яка кількість), використовуючи prompt. Коли користувач натискає "Скасувати"
//  у вікні prompt, введення об'єкта закінчується і об'єкт додається до масиву. 
//  Перед введенням наступного об'єкта користувачеві задається питання (використовуючи confirm), 
//  чи хоче він продовжить цi тортури введення об'єктів. 
//  При ствердній відповіді знову працює введення будь-якої кількості ключів для створення нового об'єкту
// Функція має повертати створений масив із об'єктами.

function readArrayOfObjects() {
    var arrayOfObjects = []; // Ініціалізація порожнього масиву об'єктів

    while (true) {
        var objectData = {}; // Ініціалізація порожнього об'єкта для введення користувачем

        // Цикл для введення ключів та значень об'єкта
        while (true) {
            var key = prompt("Введіть ключ (натисніть 'Скасувати', щоб завершити введення об'єкта):");
            
            // Перевірка, чи користувач натиснув 'Скасувати' або залишив поле порожнім
            if (key === null || key === "") {
                break;
            }

            var value = prompt("Введіть значення для ключа '" + key + "':");
            objectData[key] = value;
        }

        // Додаємо об'єкт до масиву
        arrayOfObjects.push(objectData);

        // Питання користувачеві про бажання продовжити введення об'єктів
        var continueInput = confirm("Бажаєте додати ще один об'єкт?");

        // Якщо користувач натиснув "Скасувати" або не бажає продовжити, вихід з циклу
        if (!continueInput) {
            break;
        }
    }

    return arrayOfObjects; // Повертаємо масив із об'єктами
}

// Приклад виклику функції та виведення результату у консоль
var result = readArrayOfObjects();
console.log(result);
