// Вводимо кількість рядків та кількість цифр у кожному рядку
let rowCount = 10;
let digitCount = 10;

// Ініціалізуємо порожній рядок
let result = "";

// Зовнішній цикл для рядків
for (let i = 0; i < rowCount; i++) {
    // Внутрішній цикл для цифр
    for (let j = 0; j < digitCount; j++) {
        result += j;
    }
    // Додаємо перенос рядка, крім останнього рядка
    if (i < rowCount - 1) {
        result += "\n";
    }
}

