// Створіть нескінченний цикл, який переривається за допомогою конструкції break, коли Math.random() > 0.9. 
// Код повинен підраховувати кількість ітерацій та вивести це число за допомогою alert.

let i = 0
while (true) {
    i++
    if (Math.random() > 0.9) {
        break;
    }
}
alert("Кількість ітерацій: " + i)