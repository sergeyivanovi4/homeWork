// Сформувати наступний рядок – ромбік використовуючи цикли та змiнну size яка дорiвнює ширинi 
// та висотi отриманого рядка:
// .....#.....
// ....###....
// ...#####...
// ..#######..
// .#########.
// ###########
// .#########.
// ..#######..
// ...#####...
// ....###....
// .....#.....

// Введіть розмір ромба (необхідно парне число)
var size = parseInt(prompt("Введіть розмір ромба (парне число):"));

// Ініціалізуємо порожній рядок
var result = "";

// Зовнішній цикл для верхньої частини ромба
for (var i = 1; i <= size; i += 2) {
    var spaces = (size - i) / 2; // Кількість пробілів перед символами '#'

    // Додаємо пробіли перед символами '#'
    for (var j = 0; j < spaces; j++) {
        result += ".";
    }

    // Додаємо символи '#'
    for (var k = 0; k < i; k++) {
        result += "#";
    }

    // Додаємо перенос рядка
    result += "\n";
}

// Зовнішній цикл для нижньої частини ромба
for (var i = size - 2; i >= 1; i -= 2) {
    var spaces = (size - i) / 2; // Кількість пробілів перед символами '#'

    // Додаємо пробіли перед символами '#'
    for (var j = 0; j < spaces; j++) {
        result += ".";
    }

    // Додаємо символи '#'
    for (var k = 0; k < i; k++) {
        result += "#";
    }

    // Додаємо перенос рядка
    result += "\n";
}

// Виведемо отриманий рядок у консоль
console.log(result);
