// Підрахувати суму арифметичної прогресії від 1 до N c кроком 3 (1,4,7...)
//  використовуючи цикл for. Метод Гауса не застосовувати, навпаки, зробити максимально наївне рішення.


let N = prompt("Введіть значення N:");


let sum = 0;


for (let i = 1; i <= N; i += 3) {
    sum += i;
}


console.log("Сума арифметичної прогресії від 1 до", N, "з кроком 3:", sum);



